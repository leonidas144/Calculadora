package calculadora;

public class Calculadora {
	
	//Método de soma
	public int soma (int a, int b) {
		return a + b;
	}
	
	//Método de subtração
	public int sub (int a, int b) {
		return a - b;
	}
	
	//Método de multiplicação
	public int mult (int a, int b) {
		return a * b;
	}
	
	//Método de divisão
	public int div (int a, int b) {
		return a / b;
	}

}
